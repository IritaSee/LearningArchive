#include<iostream>
#include<conio.h>
using namespace std;

int main()
{
	int x,y;
	x=5;y=5;
	
	cout<<" nilai x awal: "<<x<<endl;
	cout<<" nilai ++x: "<<++x<<endl;
	cout<<" nilai x akhir: "<<x<<endl;
	
	cout<<" nilai y awal: "<<y<<endl;
	cout<<" nilai y++: "<<y++<<endl;
	cout<<" nilai y akhir: "<<y<<endl;
	
	
}
